const kelvin=293;                               // value of variable kelvin will remain constant which is equal to 293
var celsius = kelvin-273;                       //temperature in celsius is 273 degress less than that in kelvin
var fahrenheit = celsius*(9/5)+32;              // We use this formula to conver Celsius into fahrenheit
fahrenheit= Math.floor(fahrenheit);            //we round of the temperature to its nearest smaller value
console.log("The temperature is "+fahrenheit+" degrees Fahrenheit.");
